A = float(input("Ingrese el valor del Alto (A): "))
H = float(input("Ingrese el valor del Ancho (H): "))


area = A * H
perimetro = 2 * (A + H)
superficie = area  


print(f"\resultado:")# para que haya un espacio que muestre el resultado separad

print(f"Área del rectángulo: {area}")
print(f"Perímetro del rectángulo: {perimetro}")
print(f"Superficie del rectángulo: {superficie}")
